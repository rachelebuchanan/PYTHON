#Program Description
#       A simple program to average two exam score.
#       Illustrates use of multiple input.
#
#Variables
#       score1 - first score
#       score2 - second score
#       average - average of score1 and score2

def main ( ):

#Input - get score1 and score 2
        print ("The program computes the average of two exam scores.")
        score1 = eval(input("Enter the first score: "))
        score2 = eval(input("Enter the second score: "))

#Process - calculate the average
        average = (score1 + score2)/2

#Output - print the average
        print("The average of the score is: ", average)


main()



